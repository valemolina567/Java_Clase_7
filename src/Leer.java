class Leer {
    private String nombre;
    private int edad;

    // Setter para el nombre
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    // Setter para la edad
    public void setEdad(int edad) {
        this.edad = edad;
    }

    //Getter para el nombre
    public String getNombre() {
        return nombre;
    }

    //Getter para la edad
    public int getEdad() {
        return edad;
    }
}
